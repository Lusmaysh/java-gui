public class Kelas {
    
    private String nama;
    private String jenisKelamin;
    private String kelasPelatihan;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJenisKelamin() {
        return jenisKelamin;
    }

    public void setJenisKelamin(String jenisKelamin) {
        this.jenisKelamin = jenisKelamin;
    }

    public String getKelasPelatihan() {
        return kelasPelatihan;
    }

    public void setKelasPelatihan(String kelasPelatihan) {
        this.kelasPelatihan = kelasPelatihan;
    }
    
    
            
}
